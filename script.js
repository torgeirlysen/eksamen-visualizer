// Convert date strings to Date objects and sort them
const sortedDates = Object.keys(examData).sort((a, b) => {
    const dateA = new Date(a.split('.').reverse().join('-'));
    const dateB = new Date(b.split('.').reverse().join('-'));
    return dateA - dateB;
});

const labels = sortedDates;
const data = labels.map(date => examData[date].length);

const ctx = document.getElementById('examChart').getContext('2d');
const examChart = new Chart(ctx, {
    type: 'bar',
    data: {
        labels: labels,
        datasets: [{
            label: 'Number of Exams',
            data: data,
            backgroundColor: 'rgba(75, 192, 192, 0.2)',
            borderColor: 'rgba(75, 192, 192, 1)',
            borderWidth: 1
        }]
    },
    options: {
        scales: {
            y: {
                beginAtZero: true
            }
        },
        onClick: (e) => {
            const points = examChart.getElementsAtEventForMode(e, 'nearest', { intersect: true }, true);
            if (points.length) {
                const firstPoint = points[0];
                const label = examChart.data.labels[firstPoint.index];
                showCourseDetails(label);
            }
        }
    }
});

function showCourseDetails(date) {
    const courseDetails = document.getElementById('courseDetails');
    const courses = examData[date];
    
    courseDetails.innerHTML = `<h3>Exams on ${date}:</h3><ul>` + courses.map(course => `<li>${course}</li>`).join('') + `</ul>`;
    courseDetails.style.display = 'block';
}
